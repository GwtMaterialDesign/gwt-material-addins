package gwt.material.design.incubator.client.language;

/**
 * Language object to easily define
 * the name, value and image urls to be set inside {@link LanguageSelector}
 *
 * @author kevzlou7979
 */
public class Language {

    private String name;
    private String value;
    private String image;

    public Language() {
    }

    public Language(String name, String value, String image) {
        this.name = name;
        this.value = value;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
